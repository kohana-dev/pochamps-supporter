/**
 * 앱 테마(P33). **다크가 앱의 표준 룩**(게임 유틸리티 톤 — 오버레이/아이콘과 동일한 딥 네이비).
 *
 * 시스템이 라이트여도 기본은 브랜드 다크로 렌더한다(darkTheme 기본 true). 라이트 스킴은
 * 접근성/향후 시스템 연동을 위해 함께 정의해 두되, 기본 진입 경로에선 다크를 쓴다.
 *
 * @param darkTheme 다크 스킴 사용 여부. 기본 true(앱 표준). 시스템 연동이 필요하면
 *   `isSystemInDarkTheme()` 을 넘겨 호출할 수 있다.
 * @param dynamicColor Android 12+ 다이내믹 컬러(Material You). **기본 꺼짐** — 다이내믹이
 *   브랜드 네이비/블루 아이덴티티를 덮어써 게임 유틸리티 톤이 흐려지는 것을 막는다(선택 적용).
 */
@Composable
fun PochampsTheme(
    darkTheme: Boolean = true,
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit,
) {
    val context = LocalContext.current
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> DarkColors
        else -> LightColors
    }

    val view = LocalView.current
    if (!view.isInEditMode) {
        SideEffect {
            val window = (view.context as Activity).window
            window.statusBarColor = colorScheme.background.toArgb()
            window.navigationBarColor = colorScheme.background.toArgb()
            WindowCompat.getInsetsController(window, view).apply {
                isAppearanceLightStatusBars = !darkTheme
                isAppearanceLightNavigationBars = !darkTheme
            }
        }
    }

    MaterialTheme(
        colorScheme = colorScheme,
        typography = AppTypography,
        content = content,
    )
}
